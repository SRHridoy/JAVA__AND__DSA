class bitwise_operator {
    public static void main(String[] args) {
        int a = 20;
        int b = 2;
        //Bit by bit calculate korte hobe :
        System.out.println(a&b);
        System.out.println(a|b);
        System.out.println(a^b);
        System.out.println(a<<b);
        System.out.println(a>>b);
    }
}
